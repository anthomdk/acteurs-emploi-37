                <h2>Mission locale du Chinonais: 532 contrats pour les jeunes en 2015</h2>
                <p>C’est à Cinais que s’est tenue l’assemblée générale de la Mission locale du Chinonais. C’est toujours un
                    temps fort pour l’association, qui propose de nombreux services pour accompagner les jeunes dans leur
                    insertion professionnelle. C’est également le moment de dresser le bilan des actions menées tout au long
                    de l’année.</p>
                <br>
                <p>Et ce bilan est positif. En 2015, le nombre de jeunes accueillis a augmenté de 10 %. Ce sont aujourd’hui
                    1.200 jeunes des secteurs de Sainte-Maure-de-Touraine, Richelieu, Azay- le-Rideau, L’Ile-Bouchard, Bourgueil,
                    Avoine et Chinon, qui sont accueillis par les douze conseillères. Ces dernières ont été saluées par le
                    bureau pour leur travail et leur implication de chaque jour. La directrice, Gaëlle Blusseau, a dressé
                    le bilan et présenté ces belles victoires. En 2015, 100 CDI, 432 CDD, 225 entrées en formations et 20
                    contrats en apprentissage ont été réalisés, et le premier semestre 2016 laisse entrevoir un maintien
                    de ces chiffres, sinon une progression. Un renforcement des partenariats entreprises et un travail mutualisé
                    avec les partenaires locaux (Mile, Apeb, Arefi, Agir pour l’emploi, ITS et IES…), l’adhésion à des groupes
                    d’entreprises (Adec), l’intervention aux matinales RH de la Maison de l’emploi sur l’ensemble du territoire,
                    a permis de créer de nouvelles opportunités.</p>
                <br>
                <p class="paraT2">La garantie jeune mise en place</p>
                <br>
                <p>Gaëlle Bldu, a également présenté le dispositif « Garantie jeune », qui compte à ce jour 86 jeunes de
                    toutes communes qui s’engagent pour se remobiliser vers l’emploi. Deux conseillères sont entièrement
                    dédiées à cette mission et accompagnent ces jeunes sur cinq semaines de collectif, puis en individuel
                    sur le reste de l’année. Le but étant de les aider à retrouver un emploi ou une formation. Ces cohortes
                    sont parrainées par des chefs d’entreprises ou salariés locaux, comme M. Chancel, du Prieuré Saint-Louans,
                    M. Richard, de la Chinonette, ou Mme Ravary, d’Adecco, pour le seul dernier semestre de 2015. Le président,
                    Christian Pimbert, et le vice-président, Jean Albert, tiennent également à préciser qu’alors qu’il est
                    plus que jamais impératif de lutter pour l’emploi, « les aides financières qui sous-tendent l’action
                    de cette mission de service public se contractent dangereusement. L’État, la Région, le Département réduisent
                    leur engagement nous mettant ainsi dans une situation périlleuse puisque les sollicitations et les attentes
                    croissent quand les moyens déclinent ».
                </p>
